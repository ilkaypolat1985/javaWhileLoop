public class Main {

    public static void main(String[] args) {
        System.out.println("******************************");
        int i = 1;
        while (i < 10) {
            System.out.println(i++);
        }

        System.out.println();
        System.out.println("******************************");
        i = 1;
        while (i < 10) {
            System.out.println(i);
            i += 2;
        }

        System.out.println();
        System.out.println("******************************");
        i = 2;
        while (i < 10) {
            System.out.println(i);
            i += 2;
        }


    }
}
